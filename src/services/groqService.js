function repairJsonByBalancing(text) {
  if (!text || typeof text !== 'string') return text

  const str = text.trim()
  let inString = false
  let escaped = false
  const stack = []

  for (let i = 0; i < str.length; i++) {
    const ch = str[i]

    if (inString) {
      if (escaped) {
        escaped = false
      } else if (ch === '\\') {
        escaped = true
      } else if (ch === '"') {
        inString = false
      }
      continue
    }

    if (ch === '"') {
      inString = true
      continue
    }

    if (ch === '{' || ch === '[') {
      stack.push(ch)
      continue
    }
    if (ch === '}' || ch === ']') {
      const last = stack[stack.length - 1]
      if ((ch === '}' && last === '{') || (ch === ']' && last === '[')) {
        stack.pop()
      }
    }
  }

  let repaired = str
  if (inString) {
    // Best-effort: close the last unterminated string.
    repaired += '"'
  }

  // Remove dangling comma at end before closing
  repaired = repaired.replace(/,\s*$/g, '')

  for (let i = stack.length - 1; i >= 0; i--) {
    repaired += stack[i] === '{' ? '}' : ']'
  }

  return repaired
}

